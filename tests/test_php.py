class TestPhpSymbolResolution:
    def test_resolve_use_to_file(self, tmp_path: Path) -> None:
        clear_composer_cache()
        composer = {"autoload": {"psr-4": {"App\\": "src/"}}}
        (tmp_path / "composer.json").write_text(json.dumps(composer))
        src_dir = tmp_path / "src" / "Services"
        src_dir.mkdir(parents=True)
        helper_file = src_dir / "UserService.php"
        helper_file.write_text("<?php\nnamespace App\\Services;\n")

        imports = [Import(module="App\\Services\\UserService", names=["UserService"])]
        result = resolve_symbol_definition("UserService", imports, tmp_path, None, Language.PHP)
        assert result is not None
        assert result == helper_file

    def test_resolve_unresolvable_returns_none(self, tmp_path: Path) -> None:
        clear_composer_cache()
        imports = [Import(module="App\\Unknown\\Thing", names=["Thing"])]
        result = resolve_symbol_definition("Thing", imports, tmp_path, None, Language.PHP)
        assert result is None

    def test_resolve_no_matching_import(self, tmp_path: Path) -> None:
        imports = [Import(module="App\\Other\\Class", names=["Class"])]
        result = resolve_symbol_definition("Helper", imports, tmp_path, None, Language.PHP)
        assert result is None


