class TestRubyFirstPartyDetection:
    def test_require_relative_always_first_party(self) -> None:
        root = Path("/project")
        assert is_first_party("./helper", root, [], Language.RUBY, is_relative=True) is True

    def test_stdlib_not_first_party(self) -> None:
        root = Path("/project")
        assert is_first_party("json", root, [], Language.RUBY) is False

    def test_gem_from_gemfile_not_first_party(self, tmp_path: Path) -> None:
        clear_gemfile_cache()
        (tmp_path / "Gemfile").write_text("source 'https://rubygems.org'\ngem 'rails'\ngem 'puma'\n")
        assert is_first_party("rails", tmp_path, [], Language.RUBY) is False

    def test_first_party_by_local_file(self, tmp_path: Path) -> None:
        clear_gemfile_cache()
        lib_dir = tmp_path / "lib"
        lib_dir.mkdir()
        (lib_dir / "my_helper.rb").write_text("# helper")
        assert is_first_party("my_helper", tmp_path, [], Language.RUBY) is True

    def test_unknown_module_without_local_file(self, tmp_path: Path) -> None:
        clear_gemfile_cache()
        assert is_first_party("nonexistent_module", tmp_path, [], Language.RUBY) is False


