# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _parse_ruby(source: str) -> Tree:
    """Parse Ruby source and return a tree-sitter Tree."""
    tree = parse_file(source, Language.RUBY)
    assert tree is not None
    return tree


def _make_diff_file(path: str) -> DiffFile:
    return DiffFile(
        old_path=path,
        new_path=path,
        hunks=[DiffHunk(old_start=1, old_count=1, new_start=1, new_count=1, lines=[("+", "x")])],
        is_new_file=False,
        is_deleted=False,
        is_binary=False,
    )


