class TestRubySymbolResolution:
    def test_resolve_require_relative(self, tmp_path: Path) -> None:
        helper_file = tmp_path / "app" / "services" / "helper.rb"
        helper_file.parent.mkdir(parents=True)
        helper_file.write_text("class Helper; end")

        current_file = tmp_path / "app" / "services" / "processor.rb"
        imports = [Import(module="./helper", is_relative=True)]
        result = resolve_symbol_definition("Helper", imports, tmp_path, current_file, Language.RUBY)
        assert result is not None
        assert result == helper_file

    def test_resolve_require_to_lib(self, tmp_path: Path) -> None:
        helper_file = tmp_path / "lib" / "my_app" / "helper.rb"
        helper_file.parent.mkdir(parents=True)
        helper_file.write_text("class Helper; end")

        imports = [Import(module="my_app/helper")]
        result = resolve_symbol_definition("Helper", imports, tmp_path, None, Language.RUBY)
        assert result is not None
        assert result == helper_file

    def test_resolve_unresolvable_returns_none(self, tmp_path: Path) -> None:
        imports = [Import(module="nonexistent")]
        result = resolve_symbol_definition("Helper", imports, tmp_path, None, Language.RUBY)
        assert result is None


