class TestRubyScopeDetection:
    def test_method_scope(self) -> None:
        tree = _parse_ruby(RUBY_MODULE_AND_CLASS)
        # Line inside `initialize` method (line 10: `@config = config`)
        scope = find_enclosing_scope(tree, 10, Language.RUBY)
        assert scope is not None
        assert scope.type == "method"
        assert scope.name == "initialize"

    def test_method_with_arguments(self) -> None:
        tree = _parse_ruby(RUBY_MODULE_AND_CLASS)
        # Line inside `process` method (line 14: `Helper.validate(data)`)
        scope = find_enclosing_scope(tree, 14, Language.RUBY)
        assert scope is not None
        assert scope.type == "method"
        assert scope.name == "process"

    def test_class_method_scope(self) -> None:
        tree = _parse_ruby(RUBY_MODULE_AND_CLASS)
        # Line inside `self.default` method (line 19: `new({})`)
        scope = find_enclosing_scope(tree, 19, Language.RUBY)
        assert scope is not None
        assert scope.type == "method"
        assert scope.name == "default"

    def test_class_scope(self) -> None:
        tree = _parse_ruby(RUBY_MODULE_AND_CLASS)
        # Line inside class body but not in method (line 7: `attr_reader :config`)
        scope = find_enclosing_scope(tree, 7, Language.RUBY)
        assert scope is not None
        assert scope.type == "class"
        assert scope.name == "Processor"

    def test_class_with_inheritance(self) -> None:
        tree = _parse_ruby(RUBY_INHERITANCE)
        # Line inside class (line 2: `before_action :authenticate_user!`)
        scope = find_enclosing_scope(tree, 2, Language.RUBY)
        assert scope is not None
        assert scope.type == "class"
        assert scope.name == "ApplicationController"

    def test_module_scope(self) -> None:
        tree = _parse_ruby(RUBY_MODULE_AND_CLASS)
        # Line 5: module Services (the Services module contains the class)
        # Line 5 is `module Services` — inside outer MyApp module
        scope = find_enclosing_scope(tree, 5, Language.RUBY)
        assert scope is not None
        assert scope.type == "module"

    def test_nested_module_scope(self) -> None:
        tree = _parse_ruby(RUBY_MODULE_AND_CLASS)
        # The inner Services module contains the class — check a line inside Services but outside Processor
        # Since Processor fills all of Services body, just check that module detection works
        scope = find_enclosing_scope(tree, 4, Language.RUBY)
        assert scope is not None
        assert scope.type == "module"
        assert scope.name == "MyApp"

    def test_do_block_scope(self) -> None:
        tree = _parse_ruby(RUBY_BLOCKS)
        # Line inside do block (line 4: `puts item`)
        scope = find_enclosing_scope(tree, 4, Language.RUBY)
        assert scope is not None
        assert scope.type == "block"

    def test_brace_block_scope(self) -> None:
        tree = _parse_ruby(RUBY_BLOCKS)
        # Line with `items.map { |x| x.upcase }` (line 7)
        scope = find_enclosing_scope(tree, 7, Language.RUBY)
        assert scope is not None
        # Could be block or method depending on nesting
        assert scope.type in ("block", "method")

    def test_lambda_scope(self) -> None:
        tree = _parse_ruby(RUBY_LAMBDA)
        # Line 3: `add: ->(a, b) { a + b },` — the lambda is on this line
        scope = find_enclosing_scope(tree, 3, Language.RUBY)
        assert scope is not None
        # Lambda, block, or class are all valid depending on how tree-sitter nests it
        assert scope.type in ("lambda", "block", "class")

    def test_no_scope_at_top_level(self) -> None:
        source = "x = 1\n"
        tree = _parse_ruby(source)
        scope = find_enclosing_scope(tree, 1, Language.RUBY)
        assert scope is None


