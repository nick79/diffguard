class TestRubyGeneratedFileDetection:
    def test_skip_db_schema_rb(self) -> None:
        assert is_generated_file("db/schema.rb", [], Language.RUBY) is True

    def test_skip_auto_generated_header(self) -> None:
        lines = RUBY_GENERATED.splitlines()
        assert is_generated_file("app/models/generated.rb", lines, Language.RUBY) is True

    def test_normal_ruby_not_generated(self) -> None:
        lines = RUBY_MODULE_AND_CLASS.splitlines()
        assert is_generated_file("app/services/processor.rb", lines, Language.RUBY) is False

    def test_skip_generated_by_header(self) -> None:
        lines = ["# Generated by some tool", "class Foo; end"]
        assert is_generated_file("lib/foo.rb", lines, Language.RUBY) is True

    def test_filter_analyzable_skips_generated_file(self, tmp_path: Path) -> None:
        """A Ruby file with auto-generated header should be filtered out by the pipeline."""
        models_dir = tmp_path / "app" / "models"
        models_dir.mkdir(parents=True)
        (models_dir / "generated.rb").write_text(RUBY_GENERATED)

        rel_path = "app/models/generated.rb"
        diff_files = [_make_diff_file(rel_path)]
        config = DiffguardConfig()
        result = _filter_analyzable_files(diff_files, config, project_root=tmp_path)
        assert len(result) == 0


