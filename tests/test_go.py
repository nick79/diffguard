class TestGoSymbolResolution:
    @pytest.fixture(autouse=True)
    def _clear_cache(self) -> None:
        clear_go_mod_cache()

    def test_resolve_first_party_import(self, tmp_path: Path) -> None:
        go_mod = tmp_path / "go.mod"
        go_mod.write_text("module github.com/myorg/myapp\n\ngo 1.21\n")

        # Create the package directory with a Go file
        pkg_dir = tmp_path / "internal" / "utils"
        pkg_dir.mkdir(parents=True)
        go_file = pkg_dir / "utils.go"
        go_file.write_text("package utils\n")

        imports = [Import(module="github.com/myorg/myapp/internal/utils", is_relative=False)]
        result = resolve_go_symbol("utils", imports, tmp_path)
        assert result is not None
        assert result.name == "utils.go"

    def test_resolve_skips_test_files(self, tmp_path: Path) -> None:
        go_mod = tmp_path / "go.mod"
        go_mod.write_text("module github.com/myorg/myapp\n\ngo 1.21\n")

        pkg_dir = tmp_path / "internal" / "utils"
        pkg_dir.mkdir(parents=True)
        (pkg_dir / "utils_test.go").write_text("package utils\n")
        (pkg_dir / "utils.go").write_text("package utils\n")

        imports = [Import(module="github.com/myorg/myapp/internal/utils", is_relative=False)]
        result = resolve_go_symbol("utils", imports, tmp_path)
        assert result is not None
        assert result.name == "utils.go"

    def test_resolve_third_party_returns_none(self, tmp_path: Path) -> None:
        go_mod = tmp_path / "go.mod"
        go_mod.write_text("module github.com/myorg/myapp\n\ngo 1.21\n")

        imports = [Import(module="github.com/other/pkg", is_relative=False)]
        result = resolve_go_symbol("pkg", imports, tmp_path)
        assert result is None

    def test_resolve_blank_import_skipped(self, tmp_path: Path) -> None:
        go_mod = tmp_path / "go.mod"
        go_mod.write_text("module github.com/myorg/myapp\n\ngo 1.21\n")

        imports = [Import(module="github.com/lib/pq", alias="_", is_relative=False)]
        result = resolve_go_symbol("pq", imports, tmp_path)
        assert result is None

    def test_resolve_aliased_import(self, tmp_path: Path) -> None:
        go_mod = tmp_path / "go.mod"
        go_mod.write_text("module github.com/myorg/myapp\n\ngo 1.21\n")

        pkg_dir = tmp_path / "internal" / "utils"
        pkg_dir.mkdir(parents=True)
        (pkg_dir / "utils.go").write_text("package utils\n")

        imports = [Import(module="github.com/myorg/myapp/internal/utils", alias="u", is_relative=False)]
        result = resolve_go_symbol("u", imports, tmp_path)
        assert result is not None


