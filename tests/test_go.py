class TestGoSymbolUsage:
    def test_used_symbols_in_function(self) -> None:
        tree = _parse_go(GO_PACKAGE)
        # Process method body: uses helper, strings
        symbols = find_go_used_symbols(tree, 21, 23)
        assert "helper" in symbols
        assert "strings" in symbols

    def test_used_symbols_exclude_builtins(self) -> None:
        source = """\
package main

func foo() {
    x := make([]int, 10)
    y := len(x)
    _ = y
}
"""
        tree = _parse_go(source)
        symbols = find_go_used_symbols(tree, 4, 6, exclude_builtins=True)
        assert "make" not in symbols
        assert "len" not in symbols

    def test_defined_symbols_excluded(self) -> None:
        source = """\
package main

func process() {
    x := 42
    y := x + 1
    _ = y
}
"""
        tree = _parse_go(source)
        # x is defined via short var declaration on line 4, then used on line 5
        symbols = find_go_used_symbols(tree, 4, 6)
        # x is defined locally, should not appear as external
        assert "x" not in symbols

    def test_selector_field_excluded(self) -> None:
        tree = _parse_go(GO_PACKAGE)
        # `helper.Sanitize` — Sanitize should be excluded as selector field
        symbols = find_go_used_symbols(tree, 21, 23)
        assert "Sanitize" not in symbols
        assert "TrimSpace" not in symbols

    def test_via_dispatcher(self) -> None:
        tree = _parse_go(GO_PACKAGE)
        symbols = find_used_symbols(tree, 21, 23, Language.GO)
        assert "helper" in symbols


