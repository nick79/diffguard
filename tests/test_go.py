class TestGoParsing:
    def test_parse_valid_go_file(self) -> None:
        tree = _parse_go(GO_PACKAGE)
        assert tree.root_node.type == "source_file"
        assert not tree.root_node.has_error

    def test_parse_struct(self) -> None:
        tree = _parse_go(GO_PACKAGE)
        assert not tree.root_node.has_error

    def test_parse_interface(self) -> None:
        tree = _parse_go(GO_INTERFACE)
        assert not tree.root_node.has_error

    def test_parse_goroutine(self) -> None:
        tree = _parse_go(GO_GOROUTINE)
        assert not tree.root_node.has_error

    def test_parse_defer(self) -> None:
        tree = _parse_go(GO_ERROR_HANDLING)
        assert not tree.root_node.has_error

    def test_parse_channel_operations(self) -> None:
        source = """\
package main

func producer(ch chan<- int) {
    ch <- 42
}

func consumer(ch <-chan int) {
    value := <-ch
    _ = value
}
"""
        tree = _parse_go(source)
        assert not tree.root_node.has_error

    def test_parse_error_handling_pattern(self) -> None:
        tree = _parse_go(GO_ERROR_HANDLING)
        assert not tree.root_node.has_error


